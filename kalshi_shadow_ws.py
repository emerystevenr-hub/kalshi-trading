"""
KALSHI SHADOW MODE — WebSocket edition.

WebSocket-based, authenticated. No polling. Real-time book deltas + trades
on the same latency profile as production bots (~50-200ms matching engine -> us).

Architecture:
  - Auth:           RSA-PSS-SHA256 signature headers for every connection
  - Market WS:      subscribes to orderbook_delta + trade channels for our 15 markets
  - Book state:     maintains local {bid_level: size, ask_level: size} per market
                    rebuilt on snapshot, updated incrementally via deltas
  - Quote logic:    runs on every book update — computes what we'd quote
  - Fill detection: on every trade message, check if trade price crosses our
                    virtual quote. Requires taker_side match + size > 0.
  - Rewards proxy:  records distance-from-mid + size at every book update,
                    approximating Kalshi's per-second snapshot scoring.

Reads candidates from kalshi_candidates_v2.json (generated by diag_kalshi_v2.py).

Outputs: same CSVs as REST version — kalshi_shadow_fills.csv, _quotes.csv, _rewards.csv
"""

