# ── Kalshi fee model ──────────────────────────────────────────────
# Fee = rate × contracts × P × (1-P) per side
# Maker: limit order that rests in the book (doesn't cross). Cheaper.
# Taker: order that crosses the spread and executes immediately. Expensive.
# Fees are highest at P=0.5 (0.25 coefficient) and drop sharply toward tails.
FEE_RATE_MAKER = 0.0175
FEE_RATE_TAKER = 0.07


def compute_fee(contracts: float, price: float, is_taker: bool) -> float:
    """Kalshi per-side fee: rate × contracts × P × (1-P)."""
    rate = FEE_RATE_TAKER if is_taker else FEE_RATE_MAKER
    return rate * contracts * price * (1 - price)

