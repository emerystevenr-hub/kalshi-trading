def load_candidates():
    with open(CANDIDATES_PATH) as f:
        data = json.load(f)
    filtered = [m for m in data
                if m.get("yes_bid", 0) > 0 and m.get("yes_ask", 0) > 0
                and m.get("spread_ticks", 0) >= MIN_SPREAD_TICKS
                and m.get("vol_24h", 0) >= 10000]
    filtered.sort(key=lambda x: -x.get("spread_ticks", 0))
    top = filtered[:TOP_N_MARKETS]
    print(f"Loaded {len(top)} candidate markets (top-{TOP_N_MARKETS} by spread):")
    cat_counts = {}
    for m in top:
        category, cap_mult = classify_market(m["ticker"])
        s = MarketShadow(
            ticker=m["ticker"],
            title=m.get("title", "")[:60],
            tick=m.get("tick_step", 0.01),
            vol_24h=m.get("vol_24h", 0),
            category=category,
            cap_multiplier=cap_mult,
        )
        STATES[s.ticker] = s
        cat_counts[category] = cat_counts.get(category, 0) + 1
        per_market_cap = CLIP_USD * cap_mult
        gate = "[DISABLED]" if cap_mult == 0 else f"[cap=${per_market_cap:.0f}]"
        print(f"  {s.ticker:<35s} sp={m['spread_ticks']:.1f}t  tick={s.tick:.4f}  "
              f"vol=${s.vol_24h:,.0f}/d  {category:<16s} {gate}  {s.title[:35]}")
    print(f"\nCategory distribution: {cat_counts}")
    print(f"Global inventory cap: ${GLOBAL_INVENTORY_CAP_USD:.0f}")
    print(f"Turnover gate: auto-liquidate if inv>0 with <{TURNOVER_MIN_SELLS_PER_WINDOW} sells in {TURNOVER_WINDOW_MIN}min")


def ws_run():
    private_key = load_private_key()

    def subprotocol_or_header():
        # Kalshi expects auth via headers on the initial HTTP upgrade request.
        # For WS path, method is "GET" and path is the websocket URL path.
        # Derive path from WS_URL
        from urllib.parse import urlparse
        parsed = urlparse(WS_URL)
        path = parsed.path
        hdrs = sign_request(private_key, "GET", path)
        return [f"{k}: {v}" for k, v in hdrs.items()]

    while not _STOP:
        try:
            ws = websocket.WebSocketApp(
                WS_URL,
                header=subprotocol_or_header(),
                on_open=on_open,
                on_message=on_message,
                on_error=on_error,
                on_close=on_close,
            )
            ws.run_forever(ping_interval=25, ping_timeout=10)
        except Exception as e:
            print(f"  [ws exception] {e}", flush=True)
        if not _STOP:
            print("  [ws] reconnecting in 3s...", flush=True)
            time.sleep(3)


